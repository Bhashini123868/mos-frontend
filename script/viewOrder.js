function loadTable() {
  fetch("http://localhost:8085/mos/order/get-all", {
    method: "GET",
    redirect: "follow",
  })
    .then((response) => response.json())
    .then((result) => {
      let tblBody = document.getElementById("PlacedTable");
      let body = "";


      result.forEach((element) => {
        body += `
          <tr>
            <td>${element.orderID}</td>
            <td>${element.cusID}</td>
            <td>${element.total}</td>
            <td>${element.date}</td>
            <td>
              <button class="btn btn-sm btn-primary" onclick="printBill(${
                element.orderID
              })">Print</button>
              <button class="btn btn-sm btn-outline-danger" onclick="deleteItem(${
                element.orderID
              })">üóëÔ∏è</button>
            </td>
          </tr>    
        `;
      });

      tblBody.innerHTML = body;
      console.log(result);
    })
    .catch((error) => console.log("Error fetching data:", error));
}
loadTable();

//  delete Customer
function deleteItem(index) {
  const requestOptions = {
    method: "DELETE",
    redirect: "follow",
  };

  fetch(`http://localhost:8085/mos/order/delete/${index}`, requestOptions)
    .then((response) => {
      if (!response.ok) {
        throw new Error(`HTTP error! status: ${response.status}`);
      }
      return response.text();
    })
    .then((result) => {
      console.log(result);
      loadTable();
    })
    .catch((error) => {
      console.error("Error deleting customer:", error);
    });
}

async function printBill(orderID) {
  try {
    const response = await fetch(`http://localhost:8085/mos/order/get-all-byID/${orderID}`);
    const data = await response.json();

    const order = Array.isArray(data) ? data[0] : data;

    if (!order || !order.orderID || !order.cusID || !order.date || !order.total) {
      alert("Invalid order data!");
      return;
    }

    const { jsPDF } = window.jspdf;
    const doc = new jsPDF();

    doc.setFontSize(20);
    doc.setTextColor(255, 204, 0); 
    doc.text("MOS BURGER SHOP", 106, 21, null, null, "center");

    doc.setTextColor(204, 0, 0); 
    doc.text("MOS BURGER SHOP", 105, 20, null, null, "center");


    doc.setFontSize(11);
    doc.setTextColor(80, 80, 80);
    doc.text("324/01, Panadura, Colombo", 105, 28, null, null, "center");
    doc.text("Panadura, Sri Lanka", 105, 34, null, null, "center");
    doc.line(20, 38, 190, 38); 

    doc.setFontSize(13);
    doc.setTextColor( 0);
    doc.text("Order Summary", 20, 46, null, null, );

    doc.setFontSize(12);
    doc.text(` Order ID     : #${order.orderID}`, 20, 54);
    doc.text(` Customer ID  : ${order.cusID}`, 20, 62);
    doc.text(` Date         : ${order.date}`, 20, 70);

    doc.setFontSize(13);
    doc.setTextColor(0, 128, 0); 
    doc.text(`Total Amount   : Rs. ${order.total}`, 20, 82);

    doc.setFontSize(12);
    doc.setTextColor(100);
    doc.text(" Thank you for choosing MOS Burger !", 105, 100, null, null, "center");
    doc.text("Visit us again !", 105, 106, null, null, "center");

    doc.setDrawColor(200, 200, 200);
    doc.line(30, 112, 190, 112);

    doc.save(`Order_${order.orderID}.pdf`);

  } catch (error) {
    console.error("Error while generating PDF bill:", error);
    alert("Failed to generate bill. Please try again.");
  }
}
